a = 5
b = 3

# adicao

resultado = a + b
print(f"Soma: {resultado}")

# Subtracao

resultado = a - b
print(f"Subtracao: {resultado}")

# Multiplicacao

resultado = a * b
print(f"Multiplicacao: {resultado}")

# Divisao

resultado = a // b
print(f"Divisao Inteira: {resultado}")

# Resto

resultado = a % b
print(f"Resto: {resultado}")

# Exponencial

resultado = a ** b
print(f"Exponencial: {resultado}")