a = 5
b = 3

#Igualdade
resultado = a == b
print(resultado)

#Diferenca
resultado = a != b
print(resultado)

#Maior que
resultado = a > b
print(resultado)

#Menor que
resultado = a < b
print(resultado)

#Maior ou igual
resultado = a >= b
print(resultado)

#Menor ou Igual
resultado = a <= b
print(resultado)